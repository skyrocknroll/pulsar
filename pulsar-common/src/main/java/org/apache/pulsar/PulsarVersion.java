package org.apache.pulsar;

import java.io.IOException;
import java.io.InputStream;
import java.util.Properties;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

/**
 * Exposes the Pulsar build/version metadata.
 *
 * <p>Values are loaded from {@code /org/apache/pulsar/pulsar-version.properties}, a resource
 * generated by the build only when the build info is included (controlled by the
 * {@code pulsar.includeBuildInfo} Gradle property). During development the resource is typically
 * absent, in which case this class returns {@link #UNKNOWN} placeholders.
 */
public class PulsarVersion {

    public static final String UNKNOWN = "unknown";
    private static final String RESOURCE_PATH = "/org/apache/pulsar/pulsar-version.properties";

    private static final Pattern majorMinorPatchPattern = Pattern.compile("([0-9]+)\\.([0-9]+)\\.([0-9]+)(.*)");

    // Pattern for version missing the patch number: eg: 1.14-SNAPSHOT
    private static final Pattern majorMinorPatter = Pattern.compile("([0-9]+)\\.([0-9]+)(.*)");

    private static final Properties PROPERTIES = loadProperties();

    private static Properties loadProperties() {
        Properties props = new Properties();
        try (InputStream is = PulsarVersion.class.getResourceAsStream(RESOURCE_PATH)) {
            if (is != null) {
                props.load(is);
            }
        } catch (IOException e) {
            // Fall back to empty properties; getters will return UNKNOWN placeholders.
        }
        return props;
    }

    // If the version string does not contain a patch version, add one so the
    // version becomes valid according to the SemVer library (see https://github.com/zafarkhaja/jsemver).
    static String fixVersionString(String version) {
        if (null == version) {
            return null;
        }

        Matcher majorMinorPatchMatcher = majorMinorPatchPattern.matcher(version);

        if (majorMinorPatchMatcher.matches()) {
            // this is a valid version, containing a major, a minor, and a patch version (and optionally
            // a release candidate version and/or build metadata)
            return version;
        } else {
            // the patch version is missing, so add one ("0")
            Matcher matcher2 = majorMinorPatter.matcher(version);

            if (matcher2.matches()) {
                int startMajorVersion = matcher2.start(1);
                int stopMinorVersion = matcher2.end(2);
                int startReleaseCandidate = matcher2.start(3);

                String prefix = new String(version.getBytes(), startMajorVersion,
                        (stopMinorVersion - startMajorVersion));
                String patchVersion = ".0";
                String suffix = new String(version.getBytes(), startReleaseCandidate,
                        version.length() - startReleaseCandidate);

                return (prefix + patchVersion + suffix);
            } else {
                // This is an invalid version, let the JSemVer library fail when it parses it
                return version;
            }
        }
    }

    public static String getVersion() {
        return fixVersionString(PROPERTIES.getProperty("version", UNKNOWN));
    }

    public static String getGitSha() {
        String commit = PROPERTIES.getProperty("git.commit.id", "");
        if (commit.isEmpty()) {
            return UNKNOWN;
        }
        boolean dirty = Boolean.parseBoolean(PROPERTIES.getProperty("git.dirty", "false"));
        return dirty ? commit + "(dirty)" : commit;
    }

    public static String getGitBranch() {
        return PROPERTIES.getProperty("git.branch", UNKNOWN);
    }

    public static String getBuildUser() {
        String name = PROPERTIES.getProperty("git.build.user.name", "");
        String email = PROPERTIES.getProperty("git.build.user.email", "");
        if (name.isEmpty() && email.isEmpty()) {
            return UNKNOWN;
        }
        return String.format("%s <%s>", name, email);
    }

    public static String getBuildHost() {
        return PROPERTIES.getProperty("git.build.host", UNKNOWN);
    }

    public static String getBuildTime() {
        return PROPERTIES.getProperty("git.build.time", UNKNOWN);
    }
}
